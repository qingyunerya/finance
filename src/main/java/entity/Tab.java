package entity;

public class Tab {
    private String id;
    private int tab_no;
    private String tab_name;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int getTab_no() {
        return tab_no;
    }

    public void setTab_no(int tab_no) {
        this.tab_no = tab_no;
    }

    public String getTab_name() {
        return tab_name;
    }

    public void setTab_name(String tab_name) {
        this.tab_name = tab_name;
    }
}
